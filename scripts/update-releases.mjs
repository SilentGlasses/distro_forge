#!/usr/bin/env node
// Regenerates assets/data/releases.js from Debian's distro-info-data CSVs.
//
// Manual run:  node scripts/update-releases.mjs
// Auto run:    .github/workflows/update-releases.yml (weekly)
//
// Requires Node 18+ (for global fetch).

import { writeFile } from "node:fs/promises";
import { fileURLToPath, pathToFileURL } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = join(HERE, "..", "assets", "data", "releases.js");

// Canonical distro-info-data, maintained by Debian and kept in sync with
// Ubuntu releases.
const DEBIAN_CSV = "https://salsa.debian.org/debian/distro-info-data/-/raw/main/debian.csv";
const UBUNTU_CSV = "https://salsa.debian.org/debian/distro-info-data/-/raw/main/ubuntu.csv";

// --- helpers --------------------------------------------------------------

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} fetching ${url}`);
  return res.text();
}

function parseCsv(text) {
  const lines = text.replace(/\r\n?/g, "\n").split("\n").filter((l) => l.length > 0);
  if (lines.length === 0) return [];
  const header = lines[0].split(",").map((s) => s.trim());
  return lines.slice(1).map((line) => {
    const values = line.split(",");
    const obj = {};
    header.forEach((col, i) => { obj[col] = (values[i] || "").trim(); });
    return obj;
  });
}

function today() {
  return new Date().toISOString().slice(0, 10);
}

// --- classification -------------------------------------------------------

function classifyDebian(rows, now) {
  const candidates = rows.filter((r) => r.series && r.series !== "experimental");

  const sid = candidates.find((r) => r.series === "sid");

  const testing = candidates.find(
    (r) =>
      r.series !== "sid" &&
      r.created && r.created <= now &&
      (!r.release || r.release > now) &&
      (!r.eol || r.eol > now)
  );

  const supported = candidates
    .filter((r) => r.series !== "sid")
    .filter((r) => r.release && r.release <= now)
    .filter((r) => {
      const eol = r.eol;
      const eolLts = r["eol-lts"];
      const eolElts = r["eol-elts"];
      return (
        (eol && eol >= now) ||
        (eolLts && eolLts >= now) ||
        (eolElts && eolElts >= now)
      );
    })
    .sort((a, b) => (b.release || "").localeCompare(a.release || ""));

  const out = [];
  if (testing) {
    out.push({
      codename: testing.series,
      version: testing.version || "",
      status: "testing",
      securitySuite: `${testing.series}-security`,
      hasBackports: false,
    });
  }
  if (supported[0]) {
    const r = supported[0];
    out.push({
      codename: r.series,
      version: r.version,
      status: "stable",
      securitySuite: `${r.series}-security`,
      hasBackports: true,
    });
  }
  if (supported[1]) {
    const r = supported[1];
    out.push({
      codename: r.series,
      version: r.version,
      status: "oldstable",
      securitySuite: `${r.series}-security`,
      hasBackports: true,
    });
  }
  if (sid) {
    out.push({
      codename: "sid",
      version: "",
      status: "unstable",
      securitySuite: null,
      hasBackports: false,
    });
  }
  return out;
}

function classifyUbuntu(rows, now) {
  // Currently-released and still within standard support (uses `eol`, not `eol-esm`).
  const released = rows
    .filter((r) => r.series)
    .filter((r) => r.release && r.release <= now)
    .filter((r) => r.eol && r.eol >= now);

  const isLTS = (r) => /\bLTS\b/i.test(r.version);

  const lts = released.filter(isLTS).sort((a, b) => b.release.localeCompare(a.release));
  const interim = released.filter((r) => !isLTS(r)).sort((a, b) => b.release.localeCompare(a.release));

  // Keep every in-support LTS plus the latest interim (if any).
  const kept = [...lts];
  if (interim[0]) kept.push(interim[0]);
  kept.sort((a, b) => b.release.localeCompare(a.release));

  return kept.map((r) => ({
    codename: r.series,
    version: r.version.replace(/\s*LTS\s*$/i, "").trim(),
    status: isLTS(r) ? "LTS" : "interim",
    isLTS: isLTS(r),
  }));
}

// --- emit ----------------------------------------------------------------

function emit(debianReleases, ubuntuReleases) {
  const serialise = (arr) =>
    JSON.stringify(arr, null, 2)
      .split("\n")
      .map((line, i) => (i === 0 ? line : "  " + line))
      .join("\n");

  return `// Data model for supported distros, releases, mirrors and architectures.
//
// The \`releases\` arrays are AUTO-GENERATED by scripts/update-releases.mjs from
// the canonical distro-info-data CSVs (Debian & Ubuntu). To regenerate:
//     node scripts/update-releases.mjs
// A weekly GitHub Actions workflow (.github/workflows/update-releases.yml) runs
// this automatically and commits any changes back to main. The \`mirrors\`,
// \`architectures\`, and \`components\` blocks below are hand-maintained.

export const mirrors = {
  debian: {
    primary: "http://deb.debian.org/debian",
    security: "http://security.debian.org/debian-security",
    keyring: "/usr/share/keyrings/debian-archive-keyring.gpg",
  },
  ubuntu: {
    // Used for amd64 / i386.
    primary: "http://archive.ubuntu.com/ubuntu",
    // Used for non-primary archs (arm64, armhf, ppc64el, riscv64, s390x).
    ports: "http://ports.ubuntu.com/ubuntu-ports",
    // Ubuntu security lives on the same host, different suite.
    security: "http://security.ubuntu.com/ubuntu",
    keyring: "/usr/share/keyrings/ubuntu-archive-keyring.gpg",
  },
};

// \`primaryFor\` tells the generator which Ubuntu mirror host to use.
export const architectures = [
  { id: "amd64",   label: "amd64 (x86_64)",            primaryFor: "ubuntu" },
  { id: "i386",    label: "i386 (x86, 32-bit)",        primaryFor: "ubuntu" },
  { id: "arm64",   label: "arm64 (aarch64)",           primaryFor: "ports"  },
  { id: "armhf",   label: "armhf (ARM hard-float)",    primaryFor: "ports"  },
  { id: "ppc64el", label: "ppc64el (POWER LE)",        primaryFor: "ports"  },
  { id: "riscv64", label: "riscv64",                   primaryFor: "ports"  },
  { id: "s390x",   label: "s390x (IBM Z)",             primaryFor: "ports"  },
];

export const debian = {
  components: ["main", "contrib", "non-free", "non-free-firmware"],
  defaultComponents: ["main"],
  releases: ${serialise(debianReleases)},
};

export const ubuntu = {
  components: ["main", "restricted", "universe", "multiverse"],
  defaultComponents: ["main", "restricted", "universe", "multiverse"],
  releases: ${serialise(ubuntuReleases)},
};

export const distros = { debian, ubuntu };
`;
}

// --- entry point ----------------------------------------------------------

async function readExistingReleases() {
  try {
    const url = pathToFileURL(OUT_PATH).href + `?t=${Date.now()}`;
    const mod = await import(url);
    return {
      debian: Array.isArray(mod?.debian?.releases) ? mod.debian.releases : [],
      ubuntu: Array.isArray(mod?.ubuntu?.releases) ? mod.ubuntu.releases : [],
    };
  } catch {
    return { debian: [], ubuntu: [] };
  }
}

function diffReleases(oldList, newList) {
  const byCode = (list) => new Map(list.map((r) => [r.codename, r]));
  const oldMap = byCode(oldList);
  const newMap = byCode(newList);
  const added = [], removed = [], changed = [];
  for (const [code, nr] of newMap) {
    const or = oldMap.get(code);
    if (!or) { added.push(nr); continue; }
    if (or.status !== nr.status) changed.push({ code, from: or.status, to: nr.status });
  }
  for (const [code, or] of oldMap) if (!newMap.has(code)) removed.push(or);
  return { added, removed, changed };
}

function formatDiff(label, d) {
  const lines = [`### ${label}`];
  if (!d.added.length && !d.removed.length && !d.changed.length) {
    lines.push("_No changes._");
    return lines.join("\n");
  }
  for (const r of d.added) lines.push(`- ➕ **${r.codename}**${r.version ? ` (${r.version})` : ""} — ${r.status}`);
  for (const r of d.removed) lines.push(`- ➖ **${r.codename}**${r.version ? ` (${r.version})` : ""} — was ${r.status}`);
  for (const c of d.changed) lines.push(`- 🔄 **${c.code}** — ${c.from} → ${c.to}`);
  return lines.join("\n");
}

async function main() {
  const now = today();
  console.log(`Fetching distro-info-data for ${now}\u2026`);
  const [debianCsv, ubuntuCsv, existing] = await Promise.all([
    fetchText(DEBIAN_CSV),
    fetchText(UBUNTU_CSV),
    readExistingReleases(),
  ]);
  const debianRows = parseCsv(debianCsv);
  const ubuntuRows = parseCsv(ubuntuCsv);
  const debian = classifyDebian(debianRows, now);
  const ubuntu = classifyUbuntu(ubuntuRows, now);

  if (debian.length === 0) throw new Error("Debian classifier produced an empty list; refusing to write.");
  if (ubuntu.length === 0) throw new Error("Ubuntu classifier produced an empty list; refusing to write.");

  const out = emit(debian, ubuntu);
  await writeFile(OUT_PATH, out, "utf8");

  const debianDiff = diffReleases(existing.debian, debian);
  const ubuntuDiff = diffReleases(existing.ubuntu, ubuntu);

  const summary = [
    `Automated release-data refresh from distro-info-data (${now}).`,
    "",
    formatDiff("Debian", debianDiff),
    "",
    formatDiff("Ubuntu", ubuntuDiff),
    "",
  ].join("\n");
  await writeFile(join(HERE, "..", ".release-notes.md"), summary, "utf8");

  console.log(`Wrote ${OUT_PATH}`);
  console.log("Debian:", debian.map((r) => `${r.codename} (${r.status})`).join(", "));
  console.log("Ubuntu:", ubuntu.map((r) => `${r.codename} ${r.version} (${r.status})`).join(", "));
  console.log("\n--- release notes ---\n" + summary);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
